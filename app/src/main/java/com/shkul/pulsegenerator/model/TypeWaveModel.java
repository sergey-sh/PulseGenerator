package com.shkul.pulsegenerator.model;

public class TypeWaveModel {

	private int imageId;
	private String name;

	public TypeWaveModel(int imageId, String name) {
		this.imageId = imageId;
		this.name = name;
	}


	public int getImageId() {
		return imageId;
	}

	public String getName() {
		return name;
	}
}
